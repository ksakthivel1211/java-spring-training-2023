
/**
 * Records class has all the storage variables
 */
class Records {
    public int year;
    public String birthDeath;
    public String region;
    public int count;

    public Records(int year, String birthDeath, String region, int count) {
        this.year = year;
        this.birthDeath = birthDeath;
        this.region = region;
        this.count = count;
    }
}